function recordKitePath() {
    var lastPoint;

    if (recordedPath.length > 0) {
        lastPoint = recordedPath[recordedPath.length - 1];
    }

    if (lastPoint == null || distance(character.real_x, character.real_y, lastPoint.x, lastPoint.y) > 50) {
        var newPoint = { x: character.real_x, y: character.real_y };

        if (lastPoint != null) {
            draw_line(lastPoint.x, lastPoint.y, newPoint.x, newPoint.y);
        }

        recordedPath.push(newPoint);
    }

    if (recordedPath.length > 2 && distance(character.real_x, character.real_y, recordedPath[0].x, recordedPath[0].y) < 50) {
        if (!finishedRecordingPath) {
            show_json(recordedPath);
            finishedRecordingPath = true;
        }
    }
}

function drawKitePath() {
	if(kitePath != null && kitePath.length > 1 && kitePathMap == character.map)
	{
		for (var i = 0; i < kitePath.length; i++) {
			var point1 = kitePath[i];

			var point2;

			if (i == kitePath.length - 1) {
				point2 = kitePath[0];
			}
			else {
				point2 = kitePath[i + 1];
			}

			draw_line(point1.x, point1.y, point2.x, point2.y);
		}
	}
}

