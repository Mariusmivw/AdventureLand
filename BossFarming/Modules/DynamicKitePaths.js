function findNearestPathPoint(x, y) {
    var closestPoint;
    var closestPointDist;
    var closestIndex;

    for (var i = 0; i < kitePath.length; i++) {
        var point = kitePath[i];
        var pointDist = distance(x, y, point.x, point.y);
        if (closestPoint == null || pointDist < closestPointDist) {
            closestPoint = point;
            closestPointDist = pointDist;
            closestIndex = i;
        }
    }

    return closestIndex;
}

function getKitePathForTarget()
{
	if(highestPriorityTarget != null)
	{
		var closestPath;
		var closestPathDistance;

		var possiblePaths = kitePaths[character.map][highestPriorityTarget.mtype];

		for(id in possiblePaths)
		{
			var path = possiblePaths[id];

			var closestPoint;
			var closestPointDistance;

			for(pointID in path)
			{
				var point = path[pointID];
				
				var dist = distance(point.x, point.y, highestPriorityTarget.real_x, highestPriorityTarget.real_y);
				
				if(closestPoint == null || closestPointDistance > dist)
				{
					closestPoint = point;
					closestPointDistance = dist;
				}
			}
			
			if(closestPathDistance == null || closestPathDistance > closestPointDistance)
			{
				closestPath = path;
				
				closestPathDistance = closestPointDistance;
			}
		}
		
		return closestPath;
	}
}

function followKitePath(followAlways) {
	if(highestPriorityTarget != null)
	{
		kitePath = getKitePathForTarget();
		kitePathMap = character.map;
	}
	
	if(kitePath != null)
	{
		if(kitePath != lastKitePath)
		{
			lastKitePath = kitePath;
			targetIndex = findNearestPathPoint(character.real_x, character.real_y);
		}
			var targetPoint = kitePath[targetIndex];
			var distToTarget = distance(character.real_x, character.real_y, targetPoint.x, targetPoint.y);
			if (distToTarget < 25) {
				
				kiteDirection = getKiteDirection();
				targetIndex = targetIndex + 1;
				if (targetIndex > kitePath.length - 1) {
					targetIndex = 0;
				}

				if (targetIndex < 0) {
					targetIndex = kitePath.length - 1;
				}

				targetPoint = kitePath[targetIndex];
			}

			var distToEnemy;
			
			if (highestPriorityTarget != null) {
				distToEnemy = distance(character.real_x, character.real_y, highestPriorityTarget.real_x, highestPriorityTarget.real_y);
			}
			if (highestPriorityTarget == null || (distToEnemy != null && distToEnemy < character.range)) {
				if (kiteLastMove == null || new Date() - kiteLastMove > kiteMinTimeBetweenMovements) {
					goToPoint(targetPoint.x, targetPoint.y, highestPriorityTarget.in, true, true);
					kiteLastMove = new Date();
				};
			}
			else {
				if (kiteLastMove == null || new Date() - kiteLastMove > kiteMinTimeBetweenMovements) {
					targetIndex = findNearestPathPoint(character.real_x, character.real_y);
					goToPoint(highestPriorityTarget.real_x, highestPriorityTarget.real_y, character.map, true, true);
					kiteLastMove = new Date();
				};
			}
		
		return true; //Has Path
	}
	
	return false; //No Path
}

function getKiteDirection() {
    if (highestPriorityTarget != null) {
        var nextNode = targetIndex + 1;

        var previousNode = targetIndex - 1;

        if (nextNode > kitePath.length - 1) {
            nextNode = 0;
        }

        if (previousNode < 0) {
            previousNode = kitePath.length - 1;
        }

        var nextDist = distance(kitePath[nextNode].x, kitePath[nextNode].y, highestPriorityTarget.real_x, highestPriorityTarget.real_y);

        var prevDist = distance(kitePath[previousNode].x, kitePath[previousNode].y, highestPriorityTarget.real_x, highestPriorityTarget.real_y);

        var distToTarget = distance(character.real_x, character.real_y, highestPriorityTarget.real_x, highestPriorityTarget.real_y);

        if (nextDist > prevDist && distToTarget > character.range * 0.75) {
            return 1;
        }
        else {
            return -1;
        }
    }

    return kiteDirection;
}

